#include <stdio.h>

void main() {

    float a = 6.4, b = 2.2;

    //Soma
    printf("A soma %.1f + %.1f = %.1f", a, b, a + b);
    printf("\n");

    //Subtração
    printf("A subtração %.1f - %.1f = %.1f", a, b, a - b);
    printf("\n");

    //Multiplicação
    printf("A multiplicação %.1f * %.1f = %.1f", a, b, a * b);
    printf("\n");

    //Divisão
    printf("A divisão %.1f / %.1f = %.1f", a, b, a / b);
    printf("\n");

     getchar();
}
